<?php

use Illuminate\Support\Facades\Route;

if (!function_exists('set_true')) {
    function set_true($uri, $output = 'true')
    {
        if (is_array($uri)) {
            foreach ($uri as $u) {
                if (Route::is($u)) {
                    return $output;
                }
            }
        } else {
            if (Route::is($uri)) {
                return $output;
            }
        }
    }
}

